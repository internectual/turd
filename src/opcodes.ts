// opcodes.ts — Opcode mappings per engine target
// Ported from Elletra/dso-sharp Versions/*/Ops.cs

export interface OpsMap {
  values: Record<string, number>;
  tags: Record<number, string>;
  invalid: number;
}

function buildOps(record: Record<string, number | null>, invalid: number): OpsMap {
  const values: Record<string, number> = {};
  const tags: Record<number, string> = {};
  for (const [tag, val] of Object.entries(record)) {
    if (val !== null && val !== invalid) {
      values[tag] = val;
      tags[val] = tag;
    }
  }
  return { values, tags, invalid };
}

